#' lotr: Lord of the Rings - Color Palettes
#'
#' A collection of color palettes inspired by locations, characters, and iconic 
#' scenes from J.R.R. Tolkien's Middle-earth. Includes both continuous palettes 
#' (light to dark) for heatmaps and sequential data, and discrete palettes for 
#' categorical data visualization. Provides easy integration with ggplot2.
#'
#' @section Main Functions:
#' \itemize{
#'   \item \code{\link{lotr_palette}}: Get colors from a specific palette
#'   \item \code{\link{lotr_palette_names}}: List all available palettes
#'   \item \code{\link{scale_color_lotr_d}}: ggplot2 discrete color scale
#'   \item \code{\link{scale_color_lotr_c}}: ggplot2 continuous color scale
#'   \item \code{\link{scale_fill_lotr_d}}: ggplot2 discrete fill scale
#'   \item \code{\link{scale_fill_lotr_c}}: ggplot2 continuous fill scale
#' }
#'
#' @section Available Palettes:
#' \itemize{
#'   \item \strong{shire}: Warm greens and light browns
#'   \item \strong{mordor}: Ashy blacks and volcanic reds
#'   \item \strong{rivendell}: Ethereal blues and sparkling teals
#'   \item \strong{minasmorgul}: Glowing greens and deep blacks
#'   \item \strong{aragorn}: Kingly silvers and calm blues
#'   \item \strong{samwise}: Soft pinks and rosey reds
#'   \item \strong{gollum}: Murky blues and sickly greens
#'   \item \strong{legolas}: Forest greens and bright yellows
#'   \item \strong{sauron}: Deceiving golds and lava reds
#' }
#'
